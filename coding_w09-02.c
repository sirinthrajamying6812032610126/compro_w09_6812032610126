#include <stdio.h> // นำเข้าไลบรารีสำหรับการใช้งานฟังก์ชันมาตรฐาน

int main() { // ฟังก์ชันหลักของโปรแกรมที่เป็นจุดเริ่มต้นการทำงาน
    int sum, p, x, y; // ประกาศตัวแปรสำหรับผลรวม, ผลคูณ, และตัวแปรในลูป
    sum = 0; // กำหนดค่าเริ่มต้นของ sum เป็น 0

    // ลูป For เพื่อคำนวณผลรวมของ x * y
    // x และ y เริ่มต้นที่ 1 และจะทำงานตราบเท่าที่ผลคูณของ x และ y ยังน้อยกว่าหรือเท่ากับ 15
    // ในแต่ละรอบ x และ y จะถูกเพิ่มขึ้นทีละ 1
    for (x = 1, y = 1; x * y <= 15; x++, y++) {
        p = x * y;      // คำนวณผลคูณของ x และ y และเก็บไว้ในตัวแปร p
        sum = sum + p;    // นำผลคูณ (p) ไปบวกเพิ่มกับค่า sum
        printf("x = %d, y = %d, product = %d, current sum = %d\n", x, y, p, sum);
        // แสดงค่าของ x, y, ผลคูณ (p) และผลรวมปัจจุบัน (sum) ในแต่ละรอบ
    }
    printf("\nSummation of x * y = %d\n", sum); // เมื่อลูปทำงานเสร็จสิ้นแล้ว ให้แสดงผลรวมสุดท้าย
    return 0; // ส่งค่ากลับ 0 เพื่อบอกว่าการทำงานของโปรแกรมเสร็จสมบูรณ์
}

// การอธิบายการทำงาน
// โปรแกรมจะเริ่มต้นด้วยการตั้งค่าตัวแปร sum เป็น 0 จากนั้นจะเข้าสู่ลูป for ซึ่งจะทำตามขั้นตอนดังนี้:
// การเริ่มต้น: กำหนดค่าเริ่มต้นของ x เป็น 1 และ y เป็น 1
// เงื่อนไข: โปรแกรมจะตรวจสอบเงื่อนไข x * y <= 15 หากเป็นจริง ลูปจะทำงานต่อ
// การทำงานในลูป: นำค่า p ไปบวกเพิ่มใน sum แสดงค่าของ x, y, p และ sum เพิ่มค่า x และ y ขึ้นทีละ 1
// รอบที่ 1 2 3 4
// ค่าเริ่มต้น x 1 2 3 4
// ค่าเริ่มต้น y 1 2 3 4
// x * y <= 15? 1 * 1 = 1 (จริง) 2 * 2 = 4 (จริง) 3 * 3 = 9 (จริง) 4 * 4 = 16 (เท็จ)
// p = x * y 1 4 9 -
// sumใหม่ 0 + 1 = 1 , 1 + 4 = 5 , 5 + 9 = 14 , -
// ในรอบที่ 4 ค่า x คือ 4 และ y คือ 7 ซึ่งทำให้ผลคูณ x*y เท่ากับ 28 เงื่อนไข 28 <= 15 จึงเป็น เท็จ ทำให้ลูปหยุดทำงานทันที

// สรุปผลอภิปราย
// จากการรันโค้ด จะเห็นว่าค่าของ x และ y จะเพิ่มขึ้นทีละ 1 ในแต่ละรอบการทำงานของลูป จนกระทั่งผลคูณของทั้งสองตัวมีค่าเกิน 15 ซึ่งเป็นเงื่อนไขที่กำหนดไว้ในลูป
// รอบที่ 1: x=1 และ y=1 ทำให้ x*y = 1 ผลรวมปัจจุบันจึงเป็น 1
// รอบที่ 2: x=2 และ y=2 ทำให้ x*y = 4 ซึ่งนำไปบวกเพิ่มกับผลรวมเดิม ทำให้ผลรวมเป็น 1 + 4 = 5
// รอบที่ 3: x=3 และ y=3 ทำให้ x*y = 9 ซึ่งนำไปบวกเพิ่มกับผลรวมเดิม ทำให้ผลรวมเป็น 5 + 9 = 14
// รอบที่ 4: x=4 และ y=4 ทำให้ x*y = 16 ซึ่งมีค่า มากกว่า 15 ทำให้เงื่อนไขในลูปเป็นเท็จ โปรแกรมจึงหยุดทำงานทันที
// ดังนั้น ผลลัพธ์สุดท้ายที่ได้คือ 14 ซึ่งเป็นผลรวมของ 1 + 4 + 9  การทำงานของลูป for ในโค้ดนี้จึงเป็นไปตามหลักการที่ว่า
// ตัวแปร x และ y จะถูกเพิ่มค่าไปเรื่อยๆ และการทำงานจะสิ้นสุดลงเมื่อเงื่อนไขที่กำหนดไว้ไม่เป็นจริงอีกต่อไป