#include <stdio.h> // นำเข้าไลบรารีสำหรับการใช้งานฟังก์ชันมาตรฐาน

int main() { // ฟังก์ชันหลักของโปรแกรมที่เป็นจุดเริ่มต้นการทำงาน
    int i = 1; // ประกาศและกำหนดค่าเริ่มต้นให้ตัวแปร i
    do { // ลูป do-while ที่จะทำงานอย่างน้อยหนึ่งครั้งก่อนตรวจสอบเงื่อนไข
        if (i == 6) { // เงื่อนไข: ถ้า i มีค่าเท่ากับ 6 ให้ข้ามการทำงานที่เหลือในลูปนี้ไป
            i++; // เพิ่มค่า i เพื่อไม่ให้เกิด infinite loop (ลูปไม่สิ้นสุด)
            continue; // ข้ามไปรอบต่อไปของลูปทันที
        }
        printf("%d\n", i); // พิมพ์ค่าของ i ออกทางหน้าจอ
        i++; // เพิ่มค่า i ในแต่ละรอบ
    } while (i <= 10); // เงื่อนไข: ลูปจะทำงานจนกว่า i จะมีค่ามากกว่า 10
    
    return 0; // ส่งค่ากลับ 0 เพื่อบอกว่าการทำงานของโปรแกรมเสร็จสมบูรณ์
}

// คำอธิบายการทำงานของทั้ง 3 โปรแกรม
// ทั้งสามโปรแกรมมีเป้าหมายเดียวกันคือการพิมพ์ตัวเลขตั้งแต่ 1 ถึง 10 ออกมา แต่จะข้ามการพิมพ์เลข 6 โดยใช้คำสั่ง continue โดยมีความแตกต่างกันที่โครงสร้างของลูปที่ใช้
// โปรแกรมต้นฉบับ (ใช้ for): ลูปนี้รวบรวมการกำหนดค่าเริ่มต้น (int i = 1;), เงื่อนไขการทำงาน (i <= 10;), และการเพิ่มค่า (i++) ไว้ในบรรทัดเดียว
// ทำให้โค้ดดูสั้นและเป็นระเบียบลูปนี้รวบรวมการกำหนดค่าเริ่มต้น (int i = 1;), เงื่อนไขการทำงาน (i <= 10;), และการเพิ่มค่า (i++) ไว้ในบรรทัดเดียว
// ทำให้โค้ดดูสั้นและเป็นระเบียบเมื่อ i มีค่าเท่ากับ 6, คำสั่ง continue จะถูกเรียกใช้ ทำให้โปรแกรม ข้าม คำสั่ง printf ในรอบนั้นไปทันที แล้วไปเพิ่มค่า i และเริ่มรอบใหม่
// โปรแกรมที่ใช้ do-while: ลูปนี้จะทำงานในบล็อก do อย่างน้อยหนึ่งครั้ง ก่อนที่จะตรวจสอบเงื่อนไข (i <= 10) ที่ท้ายลูป
// หลักการทำงานของ continue ยังคงเหมือนเดิม คือเมื่อ i เท่ากับ 6 จะข้ามการพิมพ์เลข 6 ไป แล้วตรวจสอบเงื่อนไขที่ท้ายลูปเพื่อไปทำงานในรอบถัดไป

// สรุปและอภิปรายผล
// สรุปผล:
// จากการทดลองเปลี่ยนโค้ดจากลูป for ไปเป็น while และ do-while พบว่าผลลัพธ์ที่ได้จากทั้งสามโปรแกรม เหมือนกันทุกประการ 
// คือโปรแกรมจะพิมพ์ตัวเลข 1, 2, 3, 4, 5, 7, 8, 9, 10 ออกมา โดยไม่มีเลข 6
// อภิปรายผล:
// การทดลองนี้แสดงให้เห็นว่าสามารถใช้โครงสร้างลูปที่แตกต่างกันเพื่อบรรลุผลลัพธ์เดียวกันได้ โดยการเลือกใช้ลูปแบบใดขึ้นอยู่กับความเหมาะสมของสถานการณ์
// for loop: เหมาะสำหรับงานที่ทราบจำนวนรอบที่แน่นอน
// while loop: เหมาะสำหรับงานที่ไม่ทราบจำนวนรอบที่แน่นอน และต้องการตรวจสอบเงื่อนไขก่อนการทำงาน
// do-while loop: เหมาะสำหรับงานที่ต้องการให้โค้ดทำงานอย่างน้อยหนึ่งครั้งเสมอ